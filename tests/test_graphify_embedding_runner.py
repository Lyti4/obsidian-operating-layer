def repo_root() -> Path:
    return Path(__file__).resolve().parents[1]


def make_manifest(tmp_path: Path) -> tuple[Path, Path]:
    repo = repo_root()
    sandbox = repo / "out" / "sandbox-vaults" / f"runner-{tmp_path.name}"
    if sandbox.exists():
        shutil.rmtree(sandbox)
    (sandbox / "Notes").mkdir(parents=True)
    (sandbox / "Notes" / "Alpha.md").write_text("# Alpha\nSafety apply graph graph\n", encoding="utf-8")
    (sandbox / "Notes" / "Beta.md").write_text("# Beta\nBackup verify graph\n", encoding="utf-8")
    graph_dir = repo / "out" / "reports" / "graphify-embedding-handoff" / f"runner-{tmp_path.name}" / "graphify-out"
    graph_dir.mkdir(parents=True, exist_ok=True)
    graph = {
        "directed": True,
        "multigraph": False,
        "graph": {},
        "nodes": [
            {"id": "a", "source_file": "Notes/Alpha.md", "community": 1},
            {"id": "b", "source_file": "Notes/Beta.md", "community": 1},
        ],
        "links": [{"source": "a", "target": "b"}],
        "hyperedges": [],
        "built_at_commit": "test",
    }
    graph_path = graph_dir / "graph.json"
    graph_path.write_text(json.dumps(graph), encoding="utf-8")
    out = repo / "out" / "reports" / "graphify-embedding-handoff" / f"runner-{tmp_path.name}" / "handoff"
    result = write_graphify_embedding_handoff(
        graph_json=graph_path,
        sandbox_vault=sandbox,
        out_dir=out,
        derived_root=repo / "out" / "external-indexing-spike" / "graphify-derived" / f"runner-{tmp_path.name}",
        max_candidates=10,
    )
    return Path(result["manifest"]), sandbox


