class FakeOllamaHandler(BaseHTTPRequestHandler):
    requests_seen: list[dict[str, object]] = []

    def do_POST(self) -> None:  # noqa: N802
        length = int(self.headers.get("Content-Length", "0"))
        payload = json.loads(self.rfile.read(length).decode("utf-8"))
        FakeOllamaHandler.requests_seen.append(payload)
        text = str(payload.get("input", ""))
        vector = [float(len(text)), 1.0, 0.5]
        body = json.dumps({"embeddings": [vector]}).encode("utf-8")
        self.send_response(200)
        self.send_header("Content-Type", "application/json")
        self.send_header("Content-Length", str(len(body)))
        self.end_headers()
        self.wfile.write(body)

    def log_message(self, _format: str, *_args: object) -> None:
        return


def test_runner_ollama_provider_uses_small_chunks(tmp_path: Path) -> None:
    FakeOllamaHandler.requests_seen = []
    try:
        server = HTTPServer(("127.0.0.1", 11434), FakeOllamaHandler)
    except OSError as exc:
        pytest.skip(f"loopback Ollama port 11434 unavailable for fake server: {exc}")
    thread = threading.Thread(target=server.serve_forever, daemon=True)
    thread.start()
    try:
        manifest, _sandbox = make_manifest(tmp_path)
        derived = repo_root() / "out" / "external-indexing-spike" / "graphify-derived" / f"ollama-{tmp_path.name}"
        if derived.exists():
            shutil.rmtree(derived)
        report = run_graphify_embedding_manifest(
            manifest_path=manifest,
            derived_root=derived,
            max_files=1,
            provider="ollama",
            resource_preflight=False,
            ollama_base_url="http://127.0.0.1:11434",
            ollama_model="bge-m3",
            max_chars_per_chunk=200,
            chunk_overlap=20,
        )
    finally:
        server.shutdown()
        server.server_close()

    assert report.provider == "ollama"
    assert report.processed == 1
    assert report.records[0].dimensions == 3
    assert report.records[0].chunk_count >= 1
    assert FakeOllamaHandler.requests_seen
    assert all(request["model"] == "bge-m3" for request in FakeOllamaHandler.requests_seen)
    assert all(len(str(request["input"])) <= 200 for request in FakeOllamaHandler.requests_seen)


