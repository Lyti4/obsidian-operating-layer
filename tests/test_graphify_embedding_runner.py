def test_ollama_adaptive_split_on_large_chunk(monkeypatch: pytest.MonkeyPatch, tmp_path: Path) -> None:
    manifest, sandbox = make_manifest(tmp_path)
    (sandbox / "Notes" / "Alpha.md").write_text("alpha " * 900, encoding="utf-8")
    # Rebuild manifest so the hash matches the larger file.
    manifest, _sandbox = make_manifest(tmp_path)
    (sandbox / "Notes" / "Alpha.md").write_text("alpha " * 900, encoding="utf-8")
    manifest_payload = json.loads(manifest.read_text(encoding="utf-8"))
    import hashlib

    alpha = sandbox / "Notes" / "Alpha.md"
    for item in manifest_payload["candidates"]:
        if item["path"] == "Notes/Alpha.md":
            item["hash"] = "sha256:" + hashlib.sha256(alpha.read_bytes()).hexdigest()
    manifest.write_text(json.dumps(manifest_payload), encoding="utf-8")

    from obslayer import graphify_embedding_runner as runner

    calls: list[int] = []

    def fake_ollama_embed(text: str, **_kwargs: object) -> tuple[list[float], int]:
        calls.append(len(text))
        if len(text) > 1200:
            raise GuardrailError("Local Ollama embedding request failed: HTTP 400: input too large")
        return [0.1, 0.2, 0.3], len(text.split())

    monkeypatch.setattr(runner, "_ollama_embed", fake_ollama_embed)
    derived = repo_root() / "out" / "external-indexing-spike" / "graphify-derived" / f"adaptive-{tmp_path.name}"
    if derived.exists():
        shutil.rmtree(derived)
    report = runner.run_graphify_embedding_manifest(
        manifest_path=manifest,
        derived_root=derived,
        max_files=1,
        provider="ollama",
        resource_preflight=False,
        max_chars_per_chunk=6000,
        chunk_overlap=200,
    )

    assert report.processed == 1
    assert report.records[0].chunk_count > 1
    assert max(calls) > 1200
    assert all(record.dimensions == 3 for record in report.records)

